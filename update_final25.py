def main():
    with open(CATALOG, "r", encoding="utf-8") as f:
        content = f.read()

    lines = content.split("\n")

    # Build replacement map: seq -> new row line
    replacements = {}
    for seq, cat, intent, knowledge, examples in DATA:
        new_row = build_row(seq, cat, intent, knowledge, examples)
        replacements[seq] = new_row

    # Process line by line
    new_lines = []
    i = 0
    while i < len(lines):
        line = lines[i]
        # Check if this line starts a table row with a sequence number we need to replace
        matched = False
        for seq in replacements:
            prefix = f"| {seq} "
            if line.startswith(prefix):
                new_lines.append(replacements[seq])
                matched = True
                break
        if not matched:
            new_lines.append(line)
        i += 1

    with open(CATALOG, "w", encoding="utf-8") as f:
        f.write("\n".join(new_lines))

    print(f"Done. Replaced {len(replacements)} entries: {sorted(replacements.keys())}")


if __name__ == "__main__":
    main()

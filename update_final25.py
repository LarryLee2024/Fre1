def build_row(seq, cat, intent, knowledge, examples):
    """Build a single catalog table row."""
    intent_text = "<br>".join(intent)
    knowledge_text = "<br>".join(knowledge)
    examples_text = "<br>".join(examples)
    return (
        f"| {seq} | {cat} | `{get_filename(seq)}` | "
        f"{intent_text}<br>{knowledge_text}<br>{examples_text} |"
    )


